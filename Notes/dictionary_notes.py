#BG 1st dictionary notes
#What are dictionaries?
#Dictionaries are a Python data structure that store information in key : value pairs inside { }.




#How do dictionaries simplify variables in a project?
#They allow you to store many related values under one single variable, making the code more organized and easier to manage.




#What are key and value pairs?
#A key is the label/name (variable), and a value is the data stored for that label. Example: age: 15


#How do you build a dictionary?
#Use {} with keys and values separated by colons.Example: person = {"name": "Alex", "age": 15}




#How do you call a value from a dictionary?
#Use the key inside square brackets.Example:person["name"




#How do you get each of the keys from a dictionary?
#Use the .keys() function.Example:person.keys()




#How do you update a value in a dictionary?
#Reassign a new value to an existing key.Example: person["age"] = 16




#Can you add new key and value pairs to a dictionary during runtime?
#Yes! Just assign a value to a new key. Example: person["school"] = "Central High")